    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        // TODO add your handling code here:
       String maThuongHieu = txt_MaThuongHieu.getText();
        String tenThuongHieu = txt_TenThuongHieu.getText();

       

        if (maThuongHieu.isEmpty() ) {
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại! Các trường dữ liệu không được bỏ trống!");
            return;
        }
        else if(thuongHieuBUS.isDuplicateMaThuongHieu(maThuongHieu)){
            JOptionPane.showMessageDialog(rootPane, "Trùng mã thương hiệu");
            return;
        }else{
        dto_ThuongHieu TH = new dto_ThuongHieu(maThuongHieu, tenThuongHieu);
        thuongHieuBUS.add(TH);
        JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
        txt_MaThuongHieu.setText("");
        txt_TenThuongHieu.setText("");
        }
        init_tbThuongHieu();
    }//GEN-LAST:event_btn_ThemActionPerformed

    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed

        int selectedIndexRowRemove = tbl_ThuongHieu.getSelectedRow();
        if (selectedIndexRowRemove == -1) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn Thương Hiệu cần xóa!");
        } else {
            // Lấy giá trị của ô tại hàng và cột đã xác định
            String maThuongHieu = tbl_ThuongHieu.getValueAt(selectedIndexRowRemove, 0).toString();
            if (!maThuongHieu.isEmpty()) {
                thuongHieuBUS.delete(maThuongHieu);
                JOptionPane.showMessageDialog(rootPane, "Xóa Thương Hiệu thành công!");
            }
            init_tbThuongHieu();
        }
    }//GEN-LAST:event_btn_XoaActionPerformed

    private void btn_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        String maThuongHieu = txt_MaThuongHieu.getText();
        String tenThuongHieu = txt_TenThuongHieu.getText();

        dto_ThuongHieu TH = new dto_ThuongHieu(maThuongHieu, tenThuongHieu);

        if (maThuongHieu.isEmpty() || tenThuongHieu.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Sửa thất bại! Các trường dữ liệu không được bỏ trống!");
            return;
        }

        thuongHieuBUS.update(TH);
        JOptionPane.showMessageDialog(rootPane, "Sửa thành công");

        init_tbThuongHieu();
    }//GEN-LAST:event_btn_SuaActionPerformed

