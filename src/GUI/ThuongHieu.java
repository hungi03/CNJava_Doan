    private void init_tbThuongHieu() {
        List<dto_ThuongHieu> DSThuongHieu = thuongHieuBUS.getAll();

        // Xóa dữ liệu cũ trên bảng
        tableModelthuongHieu.setRowCount(0);

        // Thêm dữ liệu mới vào bảng
        for (dto_ThuongHieu thuongHieu : DSThuongHieu) {
            tableModelthuongHieu.addRow(new Object[]{thuongHieu.getMaThuongHieu(), thuongHieu.getTenThuongHieu()});
        }

        tbl_ThuongHieu.setModel(tableModelthuongHieu);
        System.out.println(tableModelthuongHieu);
    }

