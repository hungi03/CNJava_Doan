    private void init_tbLoaiSanPham() {
        List<dto_LoaiSanPham> DSLoaiSanPham = loaiSanPhamBUS.getAll();

        // Xóa dữ liệu cũ trên bảng
        tableModelLoaiSanPham.setRowCount(0);

        // Thêm dữ liệu mới vào bảng
        for (dto_LoaiSanPham loaisanpham : DSLoaiSanPham) {
            tableModelLoaiSanPham.addRow(new Object[]{loaisanpham.getMaLoaiSanPham(), loaisanpham.getTenLoaiSanPham()});
        }
        
        tbl_LoaiSanPham.setModel(tableModelLoaiSanPham);
         
    }

