    private void btn_ThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ThemMouseClicked
        // TODO add your handling code here:
        String maLoai = txt_MaLoai.getText();
        String tenLoai = txt_TenLoai.getText();
        
        
        
        if(maLoai.isEmpty() || tenLoai.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane,"Thêm thất bại! Các trường dữ liệu không được bỏ trống!");
            return;
        }else if(loaiSanPhamBUS.isDuplicateMaLoai(maLoai)){
            JOptionPane.showMessageDialog(rootPane,"Trùng mã loại");
            return;
        }else{
        dto_LoaiSanPham L = new dto_LoaiSanPham(maLoai,tenLoai);
        loaiSanPhamBUS.add(L);
        JOptionPane.showMessageDialog(rootPane,"Thêm thành công");
        txt_MaLoai.setText("");
        txt_TenLoai.setText("");
        }
        
        init_tbLoaiSanPham();

    }//GEN-LAST:event_btn_ThemMouseClicked

    private void btn_XoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_XoaMouseClicked
        // TODO add your handling code here:
          int selectedIndexRowRemove = tbl_LoaiSanPham.getSelectedRow();
        if(selectedIndexRowRemove == -1) {
            JOptionPane.showMessageDialog(rootPane,"Bạn chưa chọn Loại Sản Phẩm cần xóa!");
        }
        else {
            // Lấy giá trị của ô tại hàng và cột đã xác định
            String maLoai = tbl_LoaiSanPham.getValueAt(selectedIndexRowRemove, 0).toString();
            if(!maLoai.isEmpty()) {
                loaiSanPhamBUS.delete(maLoai);
                JOptionPane.showMessageDialog(rootPane,"Xóa Loại Sản Phẩm  thành công!");
            }
            init_tbLoaiSanPham();
        }
    }//GEN-LAST:event_btn_XoaMouseClicked

    private void btn_DongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_DongMouseClicked
        // TODO add your handling code here:
          String maLoai = txt_MaLoai.getText();
        String tenLoai = txt_TenLoai.getText();

        dto_LoaiSanPham L = new dto_LoaiSanPham(maLoai, tenLoai);
        
        
        if(maLoai.isEmpty() || tenLoai.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane,"Sửa thất bại! Các trường dữ liệu không được bỏ trống!");
            return;
        }
        
        loaiSanPhamBUS.update(L);
        JOptionPane.showMessageDialog(rootPane,"Sửa thành công");
   
        init_tbLoaiSanPham();
    }//GEN-LAST:event_btn_DongMouseClicked

